import 'package:flutter/material.dart';
import 'package:gym_tracker_app/l10n/app_localizations.dart';

enum ExerciseType { strength, cardio, bodyweight }

class ExerciseInfo {
  final String id;
  final String name;
  final IconData icon;
  final String? assetPath;
  final ExerciseType type;

  final String? category;
  final List<String> equipment;
  final List<String> targetMuscles;
  final List<String> tips;
  final String? muscleImagePath;
  final bool isFrontBody;

  const ExerciseInfo({
    required this.id,
    required this.name,
    required this.icon,
    this.assetPath,
    this.type = ExerciseType.strength,
    this.category,
    this.equipment = const [],
    this.targetMuscles = const [],
    this.tips = const [],
    this.muscleImagePath,
    this.isFrontBody = true,
  });

  bool get hasCustomIcon => assetPath != null;

  static ExerciseInfo getEnterCustom(AppLocalizations loc) {
    return ExerciseInfo(
      id: '__custom__',
      name: loc.enterCustomName,
      icon: Icons.edit,
      type: ExerciseType.strength,
      category: loc
          .catFullBody, // Можеш замінити на loc.customExerciseCategory, якщо він є у твоєму файлі
    );
  }
}

