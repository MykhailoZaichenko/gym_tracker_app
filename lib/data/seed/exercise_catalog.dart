enum ExerciseType { strength, cardio, bodyweight }

class ExerciseInfo {
  final String id;
  final String name;
  final IconData icon;
  final String? assetPath;
  final ExerciseType type;

  const ExerciseInfo({
    required this.id,
    required this.name,
    required this.icon,
    this.assetPath,
    this.type = ExerciseType.strength,
  });

  bool get hasCustomIcon => assetPath != null;

  static ExerciseInfo getEnterCustom(AppLocalizations loc) {
    return ExerciseInfo(
      id: '__custom__',
      name: loc.enterCustomName,
      icon: Icons.edit,
      type: ExerciseType.strength,
    );
  }
}

